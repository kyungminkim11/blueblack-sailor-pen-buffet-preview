const norm=(v='')=>String(v).normalize('NFKC').toLowerCase().replace(/[^0-9a-z가-힣ぁ-んァ-ヶ一-龠]+/g,'');
const aliases=new Map(ALIAS.map(([a,b])=>[norm(a),b]));
