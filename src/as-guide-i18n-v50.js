function asExtraLang(){const value=(document.documentElement.lang||new URLSearchParams(location.search).get('lang')||'ko').toLowerCase();if(value.includes('hant')||value.startsWith('zh-tw')||value.startsWith('zh-hk'))return'zh-Hant';if(value.startsWith('zh'))return'zh-Hans';if(value.startsWith('ja'))return'ja';if(value.startsWith('en'))return'en';return'ko';}
function asSet(node,value){if(node&&value!=null&&node.textContent!==String(value))node.textContent=String(value);}
function applyAddressLanguage(language){
  document.querySelectorAll('.as-center-meta strong').forEach(node=>{
    if(!node.dataset.originalHtml)node.dataset.originalHtml=node.innerHTML;
    const label=node.parentElement?.querySelector('b')?.dataset.asLabelKey;
    if(label==='apply')return;
    const parts=node.dataset.originalHtml.split(/<br\s*\/?\s*>/i).map(value=>value.trim()).filter(Boolean);
    if(language==='ko'||parts.length<2)node.innerHTML=node.dataset.originalHtml;
    else node.innerHTML=parts[parts.length-1];
  });
}
