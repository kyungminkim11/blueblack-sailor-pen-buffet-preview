function portalLanguage(){
  const value=(document.documentElement.lang||localStorage.getItem('blueblack-language')||'ko').toLowerCase();
  if(value.includes('hant')||value.startsWith('zh-tw')||value.startsWith('zh-hk'))return'zh-Hant';
  if(value.startsWith('zh'))return'zh-Hans';
  if(value.startsWith('ja'))return'ja';
  if(value.startsWith('en'))return'en';
  return'ko';
}
function setText(selector,value){const node=document.querySelector(selector);if(node&&value!=null&&node.textContent!==value)node.textContent=value;}
function applyCard(href,copy){
  const card=[...document.querySelectorAll('.tool-card')].find(node=>node.getAttribute('href')?.includes(href));
  if(!card)return;
  setTextNode(card.querySelector('.tool-copy small'),copy.label);
  setTextNode(card.querySelector('.tool-copy h3'),copy.title);
  setTextNode(card.querySelector('.tool-copy p'),copy.body);
}
function setTextNode(node,value){if(node&&value!=null&&node.textContent!==value)node.textContent=value;}
function applyPortalCopy(){
  const copy=PORTAL_COPY[portalLanguage()]||PORTAL_COPY.ko;
  document.title=copy.pageTitle;
  document.querySelector('meta[name="description"]')?.setAttribute('content',copy.meta);
  document.querySelector('meta[property="og:description"]')?.setAttribute('content',copy.meta);
  setText('.portal-kicker',copy.kicker);
  setText('[data-portal-t="portalTitle"]',copy.title);
  setText('[data-portal-t="portalIntro"]',copy.intro);
  setText('.portal-hero-panel small',copy.panelKicker);
  setText('[data-portal-t="heroPanelTitle"]',copy.panelTitle);
  setText('.portal-hero-panel p',copy.panelBody);
  setText('.portal-section-head small',copy.sectionKicker);
  setText('[data-portal-t="toolsTitle"]',copy.sectionTitle);
  setText('[data-portal-t="toolsBody"]',copy.sectionBody);
  applyCard('pen-buffet',copy.cards.pen);
  applyCard('store-guide',copy.cards.store);
  applyCard('official-guide',copy.cards.official);
  applyCard('engraving-guide',copy.cards.engraving);
  applyCard('as-guide',copy.cards.service);
  applyCard('ink-price',copy.cards.ink);
  const info=document.querySelectorAll('.quick-info article');
  if(info[0]){setTextNode(info[0].querySelector('small'),copy.info.address);setTextNode(info[0].querySelector('span'),copy.info.addressNote);}
  if(info[1])setTextNode(info[1].querySelector('small'),copy.info.hours);
  if(info[2]){setTextNode(info[2].querySelector('small'),copy.info.phone);setTextNode(info[2].querySelector('span'),copy.info.phoneNote);}
  setText('.portal-footer-inner>div:first-child',copy.footer);
}

function schedulePortalCopy(){setTimeout(applyPortalCopy,0);setTimeout(applyPortalCopy,80);}
document.querySelectorAll('[data-portal-lang]').forEach(button=>button.addEventListener('click',schedulePortalCopy));
new MutationObserver(schedulePortalCopy).observe(document.documentElement,{attributes:true,attributeFilter:['lang']});
applyPortalCopy();
