function lang(){
  const v=document.documentElement.lang||'ko';
  if(v.startsWith('zh-Hant')||v.startsWith('zh-TW')||v.startsWith('zh-HK'))return'zh-Hant';
  if(v.startsWith('zh'))return'zh-Hans';
  if(v.startsWith('ja'))return'ja';
  if(v.startsWith('en'))return'en';
  return'ko';
}
function copy(){return LANG[lang()]||LANG.ko}
function apply(){
  const c=copy();
  document.title=c.pageTitle;
  const meta=q('meta[name="description"]');if(meta)meta.content=c.pageDescription;
  qa('[data-map-i18n]').forEach(n=>{const v=c[n.dataset.mapI18n];if(typeof v==='string')n.textContent=v});
  qa('[data-map-i18n-placeholder]').forEach(n=>{const v=c[n.dataset.mapI18nPlaceholder];if(typeof v==='string')n.placeholder=v});
  qa('[data-map-i18n-aria]').forEach(n=>{const v=c[n.dataset.mapI18nAria];if(typeof v==='string'){n.setAttribute('aria-label',v);n.setAttribute('title',v)}});
}
function makeBrand(name){
  const b=document.createElement('button');b.type='button';b.className='store-brand-chip';b.dataset.brand=name;b.textContent=name;b.setAttribute('aria-pressed','false');
  b.addEventListener('click',()=>{input.value=name;input.dispatchEvent(new Event('input',{bubbles:true}));input.focus({preventScroll:true})});
  return b;
}
function render(){
  BRANDS.filter(x=>x[1]).forEach(x=>popular?.append(makeBrand(x[0])));
  [...BRANDS].sort((a,b)=>a[0].localeCompare(b[0],'en')).forEach(x=>all?.append(makeBrand(x[0])));
}
function selected(value){
  const n=norm(value);
  qa('.store-brand-chip').forEach(b=>{const on=norm(b.dataset.brand)===n;b.classList.toggle('is-selected',on);b.setAttribute('aria-pressed',String(on))});
}
function updateUrl(value){
  const u=new URL(location.href);value?u.searchParams.set('brand',value):u.searchParams.delete('brand');history.replaceState(null,'',`${u.pathname}${u.search}${u.hash}`);
}
function center(){
  const doc=object?.contentDocument;if(!doc||!input?.value)return;
  const target=norm(input.value),zones=[...doc.querySelectorAll('[data-brands]')].filter(z=>norm(z.dataset.brands).includes(target));if(!zones.length)return;
  const boxes=zones.map(z=>{try{return z.getBBox()}catch{return null}}).filter(Boolean);if(!boxes.length)return;
  const x1=Math.min(...boxes.map(b=>b.x)),y1=Math.min(...boxes.map(b=>b.y)),x2=Math.max(...boxes.map(b=>b.x+b.width)),y2=Math.max(...boxes.map(b=>b.y+b.height));
  const vb=doc.documentElement.viewBox?.baseVal,r=object.getBoundingClientRect();if(!vb?.width||!r.width)return;
  const scale=r.width/vb.width;
  viewport?.scrollTo({left:Math.max(0,(x1+x2)/2*scale-viewport.clientWidth/2),top:Math.max(0,(y1+y2)/2*scale-viewport.clientHeight/2),behavior:matchMedia('(prefers-reduced-motion: reduce)').matches?'auto':'smooth'});
}
function handleInput(){
  clear?.classList.toggle('is-visible',Boolean(input.value.trim()));
  const alias=aliases.get(norm(input.value));
  if(alias&&norm(alias)!==norm(input.value)){input.value=alias;queueMicrotask(()=>input.dispatchEvent(new Event('input',{bubbles:true})));return}
  selected(input.value);updateUrl(input.value.trim());
  clearTimeout(timer);timer=setTimeout(center,180);
}
function clearSearch(){
  input.value='';input.dispatchEvent(new Event('input',{bubbles:true}));q('#storeMapFit')?.click();input.focus();
}
function fallback(text){
  const t=document.createElement('textarea');t.value=text;t.readOnly=true;t.style.cssText='position:fixed;opacity:0';document.body.append(t);t.select();const ok=document.execCommand('copy');t.remove();return ok;
}
async function copyAddress(){
  const text='BlueBlack Pen Shop, 109 Sajik-ro, Jongno-gu, Seoul, Republic of Korea';let ok=false;
  try{await navigator.clipboard.writeText(text);ok=true}catch{ok=fallback(text)}
  if(status){status.textContent=ok?copy().addressCopied:copy().addressCopyFailed;status.classList.toggle('is-error',!ok);clearTimeout(statusTimer);statusTimer=setTimeout(()=>{status.textContent='';status.classList.remove('is-error')},3500)}
}

